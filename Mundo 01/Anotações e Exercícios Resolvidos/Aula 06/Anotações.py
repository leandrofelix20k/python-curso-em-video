#Tipos Primitivos

#Quatro tipos primitivos básicos em Python são:
#int, float, bool e str

n1 = str(input('Digite um número: '))
print(type(n1))
print(n1.isnumeric())
#n1 é uma string

n2 = int(input('Digite outro número: '))
print(type(n2))
#n2 é um inteiro

n3 = float(input('Digite outro número: '))
print(type(n3))
#n4 é um ponto flutuante

n4 = bool(input('Digite outro número: '))
print(type(n4))
#n4 é um Booleano